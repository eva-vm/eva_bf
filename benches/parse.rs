fn benchmark(c: &mut Criterion) {
	let mut group = c.benchmark_group("programs");
	for (ref name, ref prog) in &PROGRAMS {
		group.throughput(Throughput::Bytes(prog.len() as u64));
		group.bench_with_input(BenchmarkId::new("parse", name), prog, |b, input| {
			b.iter(|| parse(input))
		});
	}
	group.finish();
}

criterion_group!(benches, benchmark);
criterion_main!(benches);
